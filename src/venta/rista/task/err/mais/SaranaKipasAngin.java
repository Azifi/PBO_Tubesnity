package venta.rista.task.err.mais;

public class SaranaKipasAngin {
	private int JmlKipas, KondisiKipas, PosisiKipas;
	private String StatusKonKipas, StatusPosKipas;
	
	public int getJmlKipas() {
		return JmlKipas;
	}
	public void setJmlKipas(int jmlKipas) {
		JmlKipas = jmlKipas;
	}
	public int getKondisiKipas() {
		return KondisiKipas;
	}
	public void setKondisiKipas(int kondisiKipas) {
		KondisiKipas = kondisiKipas;
	}
	public int getPosisiKipas() {
		return PosisiKipas;
	}
	public void setPosisiKipas(int posisiKipas) {
		PosisiKipas = posisiKipas;
	}
	public String getStatusKonKipas() {
		return StatusKonKipas;
	}
	public void setStatusKonKipas(String statusKonKipas) {
		StatusKonKipas = statusKonKipas;
	}
	public String getStatusPosKipas() {
		return StatusPosKipas;
	}
	public void setStatusPosKipas(String statusPosKipas) {
		StatusPosKipas = statusPosKipas;
	}
	
	
}
