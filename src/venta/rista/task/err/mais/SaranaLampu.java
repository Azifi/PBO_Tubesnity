	private String StatusKonLampu, StatusPosLampu;
	
	public int getJmlLampu() {
		return JmlLampu;
	}
	
	public int getKondisiLampu() {
		return KondisiLampu;
	}
	
	public int getPosisiLampu() {
		return PosisiLampu;
	}
	
	public String getStatusKonLampu() {
		return StatusKonLampu;
	}
	
	public String getStatusPosLampu() {
		return StatusPosLampu;
	}
	
