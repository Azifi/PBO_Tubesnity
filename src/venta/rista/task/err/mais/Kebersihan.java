package venta.rista.task.err.mais;

public class Kebersihan {
	private String SirkulaUdara;
	int NilaiCahaya, KeLembapan, Suhu;
	
	public Kebersihan(){
		
	}
	
	public String getSirkulaUdara() {
		return SirkulaUdara;
	}
	
	public void setSirkulaUdara(String sirkulaUdara) {
		SirkulaUdara = sirkulaUdara;
	}
	
	public int getNilaiCahaya() {
		return NilaiCahaya;
	}
	
	public void setNilaiCahaya(int nilaiCahaya) {
		NilaiCahaya = nilaiCahaya;
	}
	
	public int getKeLembapan() {
		return KeLembapan;
	}
	
	public void setKeLembapan(int keLembapan) {
		KeLembapan = keLembapan;
	}
	
	public int getSuhu() {
		return Suhu;
	}
	
	public void setSuhu(int suhu) {
		Suhu = suhu;
	}
	
	

}
