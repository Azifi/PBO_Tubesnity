package venta.rista.task.err.mais;

public class SaranaAC {
	private int JmlAC, KondisiAC, PosisiAC;
	private String StatusKonAC, StatusPosAC;
	
	public SaranaAC(){
		
	}
	
	public int getJmlAC() {
		return JmlAC;
	}
	public void setJmlAC(int jmlAC) {
		JmlAC = jmlAC;
	}
	public int getKondisiAC() {
		return KondisiAC;
	}
	public void setKondisiAC(int kondisiAC) {
		KondisiAC = kondisiAC;
	}
	public int getPosisiAC() {
		return PosisiAC;
	}
	public void setPosisiAC(int posisiAC) {
		PosisiAC = posisiAC;
	}
	public String getStatusKonAC() {
		return StatusKonAC;
	}
	public void setStatusKonAC(String statusKonAC) {
		StatusKonAC = statusKonAC;
	}
	public String getStatusPosAC() {
		return StatusPosAC;
	}
	public void setStatusPosAC(String statusPosAC) {
		StatusPosAC = statusPosAC;
	}
}
